package com.xworkz.dtoapp.dto;

public class FlowerDTO {

	private String name;
	private String colour;

	public FlowerDTO() {

	}

	public String getColour() {
		return colour;
	}

	public void setColour(String colour) {
		this.colour = colour;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
